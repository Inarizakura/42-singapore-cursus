#include "libft.h"

char	*strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (0 <= i)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (((void *)0));
}
