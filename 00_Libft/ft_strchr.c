#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		s++;
	}
	if (c == '\0')
		return ((char *)&s[i]);
	return (((void *)0));
}
